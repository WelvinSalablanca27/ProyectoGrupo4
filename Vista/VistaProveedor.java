    private void accionBotonGuardar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonGuardar
        String Nombre = textNombre_Proveedor.getText();
        String Telefono = textTelefono.getText();
        String Tipo = textTipo_distribuidor.getText();

        if (!Nombre.isEmpty() && !Telefono.isEmpty() && !Tipo.isEmpty()) {
            proveedorControlador.crearProveedor(Nombre, Telefono, Tipo);
            cargarDatosTabla();

            textNombre_Proveedor.setText("");
            textTelefono.setText("");
            textTipo_distribuidor.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_accionBotonGuardar

    private void accionBotonEliminar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonEliminar
        int filaSeleccionada = tablaProveedor.getSelectedRow();
        if (filaSeleccionada != -1) {
            int id_Proveedor = (int) tablaProveedor.getValueAt(filaSeleccionada, 0);
            proveedorControlador.eliminarProveedor(id_Proveedor);
            cargarDatosTabla();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila para eliminar.", "error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_accionBotonEliminar

    private void tablaProveedorMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProveedorMouseMoved
        if (evt.getClickCount() == 4) {

            int filaSeleccionada = tablaProveedor.getSelectedRow();

            if (filaSeleccionada != -1) {

                id_ProveedorSeleccionada = (int) tablaProveedor.getValueAt(filaSeleccionada, 0);
                String Nombre = (String) tablaProveedor.getValueAt(filaSeleccionada, 1);
                String Telefono = (String) tablaProveedor.getValueAt(filaSeleccionada, 2);
                String Tipo = (String) tablaProveedor.getValueAt(filaSeleccionada, 3);

                textNombre_Proveedor.setText(Nombre);
                textTelefono.setText(Telefono);
                textTipo_distribuidor.setText(Tipo);

                btnEliminar.setEnabled(false);
                btnGuardar.setEnabled(false);

            }
        }
    }//GEN-LAST:event_tablaProveedorMouseMoved

    private void accionBotonActualizar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accionBotonActualizar
        String Nombre = textNombre_Proveedor.getText();
        String Telefono = textTelefono.getText();
        String Tipo = textTipo_distribuidor.getText();

        if (!Nombre.isEmpty() && !Telefono.isEmpty() && !Tipo.isEmpty()) {

            proveedorControlador.actualizarProveedor(id_ProveedorSeleccionada, Nombre, Telefono, Tipo);
            cargarDatosTabla();

            textNombre_Proveedor.setText("");
            textTelefono.setText("");
            textTipo_distribuidor.setText("");
            id_ProveedorSeleccionada = null;

            btnEliminar.setEnabled(true);
            btnGuardar.setEnabled(true);

        } else {

            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llene los campos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_accionBotonActualizar

